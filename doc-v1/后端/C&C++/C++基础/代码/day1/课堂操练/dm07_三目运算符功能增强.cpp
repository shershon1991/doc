#include <iostream>
using namespace std;

//在C语言中，表达式的运算结果是放在寄存器里面的
//1.
//在C语言中，表达式的返回值 是变量的值
//在C++中，表达式返回的是变量的本身

//2.如何做到的
//让表达式返回一个内存空间==>内存首地址==>指针
//在C语言中，如何实现C++的效果
//3.本质
//C++编译器帮我们完成了 取地址的操作
int main701()
{
          int a = 10;
          int b = 20;

          //返回一个最小数，并且给最小数赋值成30
          //三目运算符是一个表达式，表达式不可以做左值
          (a < b ? a : b) = 30;

          printf("a = %d, b = %d \n", a, b);

          system("pause");
          return 0;
}
